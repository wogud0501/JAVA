package 연산자;

public class 산술_연산자_활용 {

	public static void main(String[] args) {
		int mathScore = 90;
		int engScore = 70;
		
		int totalScore = mathScore + engScore;              // 총점 구하기
		System.out.println(totalScore);
		
		double avgScore = totalScore / 2.0;
		System.out.println(avgScore);                      // 평균 구하기
	}

}
