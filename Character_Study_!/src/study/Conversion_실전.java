package study;

public class Conversion_실전 {

	public static void main(String[] args) {
		int iNum = 10;                // <- 10이라는 수를 int 형식으로 만들어진 iNum 이라는 변수에 대입
		float fNum = 2F;             // <- 2.0이라는 수를 float 형식으로 만들어진 fNum 이라는 변수에 2F로 대입

		double dNum;                // <- dNum이라는 변수 정의
		dNum = iNum + fNum;        // <- int 형식의 iNum과 Float 형식의 fNum을 더함
		
	
		
		System.out.println(dNum);  // <- 더한 값을 System.out.println 명령어로 출력
		

	}

}
